public class If {

	public static void main(String[] args) {
		int a = 10;
		if(a > 0)
		{
			System.out.println("positive");
		}
		else if(a < 0)
		{
			System.out.println("negative");
			
		}
		else if (a == 0)
		{
			System.out.println("Zero");
			
		}
		else {
			System.out.println("Invalid Input");
		}

	}

}
